#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	long	n;
	char	number[10];

	n = nb;
	i = 0;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n == 0)
		write(1, "0", 1);
	while (n)
	{
		number[i++] = "0123456789"[n % 10];
		n /= 10;
	}
	while (i)
		write (1, &number[--i], 1);
}
